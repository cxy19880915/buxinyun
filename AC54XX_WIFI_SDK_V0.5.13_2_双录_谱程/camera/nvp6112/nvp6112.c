/*
par: 0-->>data output VDO[7:0]
par: 1-->>data output VDO[0:7]
*/
static void data_output_order(u8 vdo_order)
{
    u8 status = 0;
    wrNVP6112Reg(0xFF, 0x01);
    rdNVP6112Reg(0xD1, &status);
    status = ((status & ~BIT(3)) | (vdo_order & 0x01) << 3);
    wrNVP6112Reg(0xD1, status);
}

/*
parm: 1 sel channel 1
parm: 2 sel channel 2
...
*/
void video_in_channel_sel(u8 channel)
{
    u8 chan[] = {0x00, 0x11, 0x22, 0x33};

    wrNVP6112Reg(0xFF, 0x01);
    wrNVP6112Reg(0xc0, chan[channel - 1]); //0x11:channel 2; 0x22:channel 3;0x33:channel 4
    wrNVP6112Reg(0xc2, chan[channel - 1]); //0x11:channel 2; 0x22:channel 3;0x33:channel 4
}

