static int wait_signal_valid()
{
    u32 i;

//    signal_val = FALSE;
    if (channel_1_signal() != NONE_SIGNAL
        || channel_2_signal() != NONE_SIGNAL) {
        //500ms 
        u32 time = jiffies + msecs_to_jiffies(500);
        while (1) {
            if (time_after(jiffies, time)) {
                puts("\n nvp6112 signal valid\n");
                return 0;
            }

        }
    } else {
        //3s
        u32 time = jiffies + msecs_to_jiffies(3000);
        while (channel_1_signal() == NONE_SIGNAL
               && channel_2_signal() == NONE_SIGNAL) {
            if (time_after(jiffies, time)) {
                puts("\n nvp6112 signal no no no valid\n");
                return -1;
            }
        }
    }

    if (channel_1_signal() != NONE_SIGNAL
        || channel_2_signal() != NONE_SIGNAL) {
        return 0;
    }
    puts("\n nvp6112 signal nonnonon no valid\n");
    return -1;
}

