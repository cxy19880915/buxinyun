static u32 reset_gpio[2] = {-1, -1};
static void *iic = NULL;

#define WRCMD (0x64 << 0)
#define RDCMD ((0x64 << 0) | BIT(0))
unsigned char wrNVP6112Reg(u16 regID, unsigned char regDat)
{
    u8 ret = 1;
    dev_ioctl(iic, IIC_IOCTL_START, 0);
    if (dev_ioctl(iic, IIC_IOCTL_TX_WITH_START_BIT, WRCMD)) {
        puts("\n iic wr err 0\n");
        ret = 0;
        goto __wend;
    }

    if (dev_ioctl(iic, IIC_IOCTL_TX, regID)) {
        puts("\n iic wr err 1\n");
        ret = 0;
        goto __wend;
    }


    if (dev_ioctl(iic, IIC_IOCTL_TX_WITH_STOP_BIT, regDat)) {
        puts("\n iic wr err 2\n");
        ret = 0;
        goto __wend;
    }

__wend:
    dev_ioctl(iic, IIC_IOCTL_STOP, 0);

    return ret;
}



unsigned char rdNVP6112Reg(u16 regID, unsigned char *regDat)
{
    u8 ret = 1;

    dev_ioctl(iic, IIC_IOCTL_START, 0);

    if (dev_ioctl(iic, IIC_IOCTL_TX_WITH_START_BIT, WRCMD)) {
        puts("\n iic rd err 0\n");
        ret = 0;
        goto __rend;
    }

    if (dev_ioctl(iic, IIC_IOCTL_TX, regID)) {
        puts("\n iic rd err 1\n");
        ret = 0;
        goto __rend;
    }

    if (dev_ioctl(iic, IIC_IOCTL_TX_WITH_START_BIT, RDCMD)) {
        puts("\n iic rd err 2\n");
        ret = 0;
        goto __rend;
    }

    dev_ioctl(iic, IIC_IOCTL_RX_WITH_STOP_BIT, (u32)regDat);

__rend:

    dev_ioctl(iic, IIC_IOCTL_STOP, 0);
    return ret;
}



