#define FILTER_CNT 10
u8 channel_1_signal()
{
    u8 reg_ec, reg_ed, i, reg_ee, reg_ef, reg_f0;
    static u8 last_signal_mode = NONE_SIGNAL;
    u8 signal_mode = NONE_SIGNAL;
    u8 signal_cnt = 0;
    u8 no_signal_cnt = 0;
    for (i = 0; i < FILTER_CNT; i++) {
        wrNVP6112Reg(0xFF, 0x01);
        rdNVP6112Reg(0xEC, &reg_ec);
        rdNVP6112Reg(0xED, &reg_ed);
        rdNVP6112Reg(0xEE, &reg_ee);
        rdNVP6112Reg(0xEF, &reg_ef);
        rdNVP6112Reg(0xF0, &reg_f0);

//        printf("\nreg_ec %x \nreg_ed %x \nreg_ee %x \nreg_ef %x \nreg_f0 %x\n", reg_ec, reg_ed, reg_ee, reg_ef, reg_f0);

        if (NVP6112_WORK_MODE == NVP6112_WORK_MODE_CVBS_27M) {
            if ((!(reg_ef & 0x01)) && (reg_ec & 0x01) == 0x01) {
//                puts("\n pal \n");
                signal_mode = PAL_VGA_25FPS;
            } else if (((reg_ef & 0x01) == true) && (reg_ec & BIT(0)) == 0x01) {
//                puts("\n ntsc \n");
                signal_mode = NTSC_VGA_30FPS;
            }
        } else if (NVP6112_WORK_MODE == NVP6112_WORK_MODE_720P) {

            if (reg_ee & BIT(0)) {
                if (reg_ee & BIT(4)) {
//                    puts("\n AHD 25P\n");
                    signal_mode = PAL_720P_25FPS;
                }
            } else {
                if (reg_ed & BIT(4)) {
//                    puts("\n AHD 30P\n");
                    signal_mode = NTSC_720P_30FPS;
                }
            }

//            if (!(reg_ee & BIT(0)))
//            {
//                if ((reg_ed & BIT(0)) && (reg_f0 & 0X03) == 0X03)
//                {
//                    //AHD 30P
//                    puts("\n AHD 30P\n");
//                    signal_mode = NTSC_720P_30FPS;
//                }
//            }
//            else
//            {
//                if ((reg_ed & BIT(0)) && (reg_f0 & 0X03) == 0X03)
//                {
//                    //AHD 25P
//                    puts("\n AHD 25P\n");
//                    signal_mode = PAL_720P_25FPS;
//                }
//            }
        }
        if (last_signal_mode == signal_mode) {
            if (++signal_cnt >= (u8)(FILTER_CNT / 2)) {
                if (signal_mode != NONE_SIGNAL) {
//                    puts("\n xxxxx kkk\n");
                    return signal_mode;
                }
            }
        } else {
            signal_cnt = 0;
        }
        last_signal_mode = signal_mode;

        if (signal_mode == NONE_SIGNAL) {
            no_signal_cnt++;
        }

    }

//    printf("\n no_signal_cnt %d\n",no_signal_cnt);
    if (no_signal_cnt == FILTER_CNT) {
//        puts("\n xxno video NONE_SIGNAL\n");
        return NONE_SIGNAL;
    }


    if (signal_mode != NONE_SIGNAL) {
//        puts("\n xxxxx on video\n");
        return signal_mode;
    }


    return NONE_SIGNAL;
}


